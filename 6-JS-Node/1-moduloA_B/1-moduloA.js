// Modularização é o ato de deixar arquivos de códigos visíveis para outros aquivos.
// No javascript existe três formas: this, export and module.exports().

this.ola = 'Fala pessoal' // This deixa visível um atributo.
exports.bemVindo = 'Bem vindo ao node!' // exports deixa visível um atributo.
module.exports.ateLogo = 'Até o próximo exemplo' // module.exports deixa visível um atributo ou um objeto com valores.